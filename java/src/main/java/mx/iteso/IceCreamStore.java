public abstract class IceCreamStore {
    public Cone prepareCone(String flavor){
    	IceCream iceCream;
        Cone cone = new Cone();
      
        iceCream = createIceCream(flavor);

        cone.prepare();
        cone.addIceCream(iceCream);
        cone.serve();
        
        return cone;
    }
    
    protected abstract IceCream createIceCream(String flavor);    

}
