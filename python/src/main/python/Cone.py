
class Cone:

    def __init__():
        self.icecreams = []

    def prepare():
        print("Making the cone")
    
    def addIceCream(iceCream):
        self.icecreams.append(iceCream)
    
    def serve():
        print("Serving...")

    def getCone():
        ic = ""
        for i in self.icecreams:
            ic += i.getName()
        return ic